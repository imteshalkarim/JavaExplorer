//Write a program named tarea1 to display the area of a triangle whose base and height is given
public class p09_tarea1 {
    public static void main(String[] args) {
        // Initialize variables for the base and height
        double base = 5.0;
        double height = 8.0;
        
        // Calculate the area of the triangle
        double area = 0.5 * base * height;
        
        // Display the area of the triangle
        System.out.println("The area of the triangle is: " + area);
    }
}
/*In this program, we first initialize two variables base and height with values 5.0 and 8.0, respectively.

Next, we calculate the area of the triangle using the formula 0.5 * base * height and store the result in a variable named area.

Finally, we display the result using System.out.println().*/